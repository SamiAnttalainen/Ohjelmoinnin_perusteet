Nimi = input("Mikä nimesi on: ")
Kkanta = int(input("Anna kolmion kanta kokonaislukuna: "))
Kkorkeus = float(input("Anna kolmion korkeus desimaalilukuna: "))
Ala = (Kkanta * Kkorkeus) / 2
print(Nimi + " annoit kannaksi " + str(Kkanta) + " ja korkeudeksi " + str(Kkorkeus))
print("Kolmion pinta-ala on tällöin " + str(Ala))
print("Kiitos ohjelman käytöstä.")
